    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        ConsultaDAO CDAO = new ConsultaDAO();
        try {
            CB_procurar.removeAllItems();
            consulta_arraylist = CDAO.Listar();

        } catch (Exception e) {
            System.out.println("problema");
        }

        for (int i = 0; i < consulta_arraylist.size(); i++) {
            CB_procurar.addItem(String.valueOf(consulta_arraylist.get(i).getId_consulta()));
        }
        RB_nao.setEnabled(false);
        RB_sim.setEnabled(false);
        RB_aberto.setEnabled(false);
    }//GEN-LAST:event_formWindowOpened

