    private void CB_procurarItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_CB_procurarItemStateChanged
        ConsultaDAO CDAO = new ConsultaDAO();
        try {

            Consulta consulta_selecionado = consulta_arraylist.get(CB_procurar.getSelectedIndex());
            String id_paciente = String.valueOf(consulta_selecionado.getId_consulta());
            int id_consulta_int = Integer.parseInt(id_paciente);
            
            Consulta consulta = CDAO.Procurar(id_consulta_int);

            if (consulta != null) {
                String hora_string;
                
                TF_id_medico.setText(String.valueOf(consulta.getId_medico()));
                TF_id_paciente.setText(String.valueOf(consulta.getId_paciente()));

                Calendar calendar = Calendar.getInstance();
                calendar.setTime(consulta.getData());
                DC_data_consulta.setSelectedDate(calendar);

                SimpleDateFormat format = new SimpleDateFormat("HH:mm");
                hora_string = format.format(consulta.getHora());
                TF_hora_consulta.setText(hora_string);


                if (consulta.getCompareceu() == 'N' || consulta.getCompareceu() == 'n') {
                    RB_nao.setSelected(true);
                    RB_sim.setSelected(false);
                    RB_aberto.setSelected(false);
                } else if (consulta.getCompareceu() == 'S' || consulta.getCompareceu() == 's') {
                    RB_nao.setSelected(false);
                    RB_sim.setSelected(true);
                    RB_aberto.setSelected(false);
                } else {
                    RB_nao.setSelected(false);
                    RB_sim.setSelected(false);
                    RB_aberto.setSelected(true);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Consulta não encontrada.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Entrada inválida. Certifique-se de que seja um número inteiro.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Problema ao procurar Consulta");
        }
    }//GEN-LAST:event_CB_procurarItemStateChanged

